/* ============================================================
   app.js — نقطة البداية الرئيسية للتطبيق
   يحتوي على: دالة init() لتهيئة التطبيق،
   دالة updateAll() لتحديث كل الشاشات دفعة واحدة،
   ربط أحداث النوافذ المنبثقة
   ============================================================ */

/* ============================================================
   تهيئة التطبيق (Initialization)
   ============================================================ */
function init() {
  // 1. تحميل البيانات المحفوظة
  load();

  // 2. إذا لم توجد حسابات → زرع الحسابات الافتراضية
  if (!S.accounts || S.accounts.length === 0) seedAccounts();

  // 3. التأكد من وجود مصفوفة الاتصالات
  if (!S.contacts) S.contacts = [];

  // 4. تحديث كل الشاشات
  updateAll();

  // 5. تعيين تاريخ اليوم في حقول التاريخ
  setToday();

  // 6. تحميل إعدادات الشركة في نموذج الإعدادات
  loadSettings();

  // 7. تطبيق حالة الشريط الجانبي (مغلق على الجوال افتراضياً)
  if (window.innerWidth <= 768) {
    sidebarOpen = false;
    applySidebar();
  }

  // 8. ربط حدث إغلاق النوافذ المنبثقة عند النقر خارجها
  document.querySelectorAll('.modal-overlay').forEach(overlay => {
    overlay.addEventListener('click', e => {
      if (e.target === overlay) overlay.classList.remove('open');
    });
  });
}

/* ============================================================
   تحديث جميع الشاشات (Update All)
   دالة مركزية تُستدعى بعد أي تغيير في البيانات
   ============================================================ */
function updateAll() {
  // ── لوحة التحكم ──────────────────────────────────────────
  renderDashboard();

  // ── القيود والدفاتر ───────────────────────────────────────
  renderJournal();
  renderLedger();
  renderTrialBalance();

  // ── القوائم المالية ───────────────────────────────────────
  renderIS();
  renderBS();
  renderCF();

  // ── المبيعات والمشتريات ───────────────────────────────────
  renderSales();
  renderPurchases();

  // ── المخزون ───────────────────────────────────────────────
  renderInventory();

  // ── العملاء والموردون ─────────────────────────────────────
  renderCustomers();
  renderSuppliers();

  // ── شجرة الحسابات ────────────────────────────────────────
  renderAccounts();

  // ── التقارير ─────────────────────────────────────────────
  renderProfitReport();
  renderInventoryReport();
  renderAgingReport();

  // ── تحديث القوائم المنسدلة ───────────────────────────────
  updateLedgerFilter();
  updateAccountParentSelects();

  // ── تحديث اسم الشركة في كل الصفحات ──────────────────────
  document.getElementById('is-company').textContent = S.settings.company;
  document.querySelectorAll('.print-page-header h2').forEach(el => el.textContent = S.settings.company);
}

/* ============================================================
   بدء التطبيق عند تحميل الصفحة
   ============================================================ */
init();
